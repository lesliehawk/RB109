# What does this code print?

5.step(to: 10, by: 3) { |value| puts value }

# Outputs 5, then 8
# Step is a iterating method.
# First iteration:
# Outputs value (5),
# 2nd iteration:
# Increments value by: 3, (8)
# Outputs value (8),
# Increments value by: 3, (11)
# Exits the loop because value to: 10 has been exceeded. (11)

